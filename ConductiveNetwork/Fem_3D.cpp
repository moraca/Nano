//---------------------------------------------------------------------------
//Node类构造函数；
Node::Node(const double ix, const double iy, const double iz)
{
	x=ix;
	y=iy;
	z=iz;
}//---------------------------------------
//---------------------------------------------------------------------------
//距离；
double Node::distance_to(const Node& n)
{
	return(sqrt( (x-n.x)*(x-n.x)+(y-n.y)*(y-n.y)+(z-n.z)*(z-n.z)) );
}//----------------------------------------

